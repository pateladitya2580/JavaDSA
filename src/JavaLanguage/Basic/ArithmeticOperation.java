package JavaLanguage.Basic;

public class ArithmeticOperation {
    static void main(String[] args) {
        int x = 5;// int x = 5,y = 2;
        int y = 2;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x%y);
    }
}
